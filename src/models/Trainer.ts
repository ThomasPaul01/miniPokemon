class Trainer {
    private name: string;
    private level: number;
    private experience: number;
    private pokemons: string[];

    constructor(name: string, level: number, experience: number, pokemons: string[]) {
        this.name = name;
        this.level = level;
        this.experience = experience;
        this.pokemons = pokemons;
    }
    
}